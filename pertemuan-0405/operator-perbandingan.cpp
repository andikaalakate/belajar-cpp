#include <iostream>
using namespace std;

int main() {
    int p = 10;
    int q = 20;

    cout << "--- Operator Perbandingan ---\n";
    cout << endl;

    cout << "Nilai P = " << p << endl;
    cout << "Nilai Q = " << q << endl;
    cout << endl;

    cout << "P == Q : " << (p == q) << endl;
    cout << "P != Q : " << (p != q) << endl;
    cout << "P > Q  : " << (p > q) << endl;
    cout << "P < Q  : " << (p < q) << endl;
    cout << "P >= Q : " << (p >= q) << endl;
    cout << "P <= Q : " << (p <= q) << endl;
    return 0;
}