#include<iostream>
using namespace std;

int main() {
	//	tipe data numerik
	//	integer
	int angka1 = 1000;
	cout << angka1 << endl;
	
	//	float
	float angka2 = 5.75;
	cout << angka2 << endl;
	
	//	double
	double angka3 = 19.99;
	cout << angka3 << endl;
	
	//	tipe data boolean
	bool senangCoding = true;
	bool ikanEnak = false;
	cout << senangCoding << endl;
	cout << ikanEnak << endl;
	
	//	tipe data char
	char gradeSaya = 'B';
	cout << gradeSaya << endl;
	
	//	tipe data string
	string salam = "Halo";
	cout << salam << endl;
}
